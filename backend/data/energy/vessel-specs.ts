export function normalizeShipName(name: string): string {
  return name.normalize("NFKC").replace(/[^\p{L}\p{N}]/gu, "").toUpperCase();
}

export function findVesselSpecByMmsi(mmsi?: string): VesselSpec | undefined {
  if (!mmsi) return undefined;
  return VESSEL_SPECS_SAMPLE.find((spec) => spec.mmsi === mmsi.trim());
}

export function findVesselSpecByImo(imo?: string): VesselSpec | undefined {
  if (!imo) return undefined;
  return VESSEL_SPECS_SAMPLE.find((spec) => spec.imo === imo.trim());
}

export function findVesselSpecByName(name?: string): VesselSpec | undefined {
  if (!name) return undefined;
  const normalized = normalizeShipName(name);
  return VESSEL_SPECS_SAMPLE.find((spec) =>
    [spec.name, spec.aisName, spec.koreanName].filter(Boolean).some((candidate) => normalizeShipName(candidate!) === normalized)
  );
}
